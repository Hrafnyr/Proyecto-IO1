   public void addNewRow(){
        
        for (int i = 0; i < variablesData.size(); i++) {
            //obtener el objeto
            String vector[]={variablesData.get(i).name,String.valueOf(variablesData.get(i).valor)};
            DefaultTableModel tb = (DefaultTableModel)this.jTable1.getModel();
            tb.addRow(vector);
            this.jTable1.setRowHeight(25);     
        }
          
    }
    
    public void Clear_T(){
        for (int i = 0; i < this.jTable1.getRowCount(); i++) {
            DefaultTableModel tb = (DefaultTableModel)this.jTable1.getModel();
            tb.removeRow(i);
            i-=1;
        }
    }
    
